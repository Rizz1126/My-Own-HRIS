// Dummy calculator that evenly distributes the 90 questions to the 20 dimensions.
// Real PAPI Kostick has a specific scoring matrix, but this simulates it for the dashboard demo.
export const calculatePapiScores = (answers) => {
  const scores = {};
  PAPI_DIMENSIONS_LIST.forEach(d => scores[d] = 0);
  
  answers.forEach((ans, index) => {
    // ans is 'a' or 'b'
    // each question maps to 2 dimensions (one for a, one for b)
    const dimA = PAPI_DIMENSIONS_LIST[(index * 2) % 20];
    const dimB = PAPI_DIMENSIONS_LIST[(index * 2 + 1) % 20];
    
    if (ans === 'a') {
      scores[dimA] += 1;
    } else if (ans === 'b') {
      scores[dimB] += 1;
    }
  });

  // Normalize scores to max 9 (PAPI uses 0-9)
  const normalized = {};
  PAPI_DIMENSIONS_LIST.forEach(d => {
    // Map whatever the max is to 0-9
    normalized[d] = Math.min(9, Math.round((scores[d] || 0) * (9 / 4.5)));
  });
  
  return normalized;
};
